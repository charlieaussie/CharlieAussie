package package2;

public class SuperMonster {
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMonsterDamage() {
        return monsterDamage;
    }

    public void setMonsterDamage(int monsterDamage) {
        this.monsterDamage = monsterDamage;
    }

    public String getMonsterAttackMessage() {
        return monsterAttackMessage;
    }

    public void setMonsterAttackMessage(String monsterAttackMessage) {
        this.monsterAttackMessage = monsterAttackMessage;
    }

    public int getMonsterHealth() {
        return monsterHealth;
    }

    public void setMonsterHealth(int monsterHealth) {
        this.monsterHealth = monsterHealth;
    }

    public int getTrollHide() {
        return trollHide;
    }

    public void setTrollHide(int trollHide) {
        this.trollHide = trollHide;
    }

    public int getMonsterXp() {
        return monsterXp;
    }

    public void setMonsterXp(int monsterXp) {
        this.monsterXp = monsterXp;
    }

    public String name;
public int monsterDamage;
public String monsterAttackMessage;
public int monsterHealth;
public int trollHide;
public int monsterXp;
}
